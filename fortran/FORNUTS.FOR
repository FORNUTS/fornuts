C
      SUBROUTINE LITTER(LBMAS,ROTNUM,FRBMAS,RTRANS,LTRANS,SOILC,SOILN,
     $                  LEAFNF,ROOTNF)
C
      REAL LBMAS,FRBMAS,SOILC,SOILN(6),RTRANS,LTRANS,LEAFNF,ROOTNF
C
      INTEGER ROTNUM
C
      SOILN(ROTNUM)=SOILN(ROTNUM)+(0.50*LBMAS*(LEAFNF/100.))*
     $              (1.-(LTRANS/100.))+(FRBMAS*(ROOTNF/100.))*
     $              (1.-(RTRANS/100.))
C
      RETURN
      END
