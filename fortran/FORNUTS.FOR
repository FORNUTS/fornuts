 2330 FORMAT(/17X,'--------',/43X,'LEACHING:        ',F9.0)
C
      ENTRNR=RAINNS+FERTNS
      WRITE(*,2340)ENTRNR
 2340 FORMAT(/9X,'TOTAL: ',F10.0,17X,'LOSSES FROM BIOMASS REMOVALS')
C
      WRITE(*,2350)NREMSS
 2350 FORMAT(/46X,'STEMS:          ',F9.2)
